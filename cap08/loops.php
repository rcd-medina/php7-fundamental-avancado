<?php

// ====================================================================================================
// Laço de repetição for.
// for(inicialição; teste; passo/incremento)
// ====================================================================================================
for ($i = 0; $i < 5; $i++) {
    echo "<p>Texto nº $i </p>";
}

// ====================================================================================================
// Barra para separar.
echo("<hr>");


// ====================================================================================================
// laço for() em contagem regressiva.
// ====================================================================================================
for ($i = 5; $i > 0; $i--) {
    echo "<p>Texto nº $i </p>";
}

echo("<hr>");

// ====================================================================================================
// Laço while()
// Este laço trabalha de maneira parecida com o laço for, ele permite condições lógicas complexas.
// ====================================================================================================
echo "Laço while()";
$i = 0;
while ($i < 10) {
    echo "<p>Texto nº $i </p>";
    $i++;
}


